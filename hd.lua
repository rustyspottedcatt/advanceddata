--[=[
	Why did I make this ? 

	I made this to be able to easily handle datastore using Knit & ProfileService, it allows you to fetch data, set data, wipe data & it is very easily configurable (profile structure is very instectinve)

	Why did I choose Profile Service ?

	ProfileService is the most reliable way to handle data, instead of using values and sending them up to a datastore, it sends up tables, that prevents data loss and is very reliable
]=]

local Players = game:GetService("Players")
local ReplicatedStorage = game:GetService("ReplicatedStorage")
local ServerScriptService = game:GetService("ServerScriptService")

local Knit = require(ReplicatedStorage.Shared.Modules.Knit)
local Promise = require(ReplicatedStorage.Shared.Modules.Promise)
local ProfileService = require(ServerScriptService.Server.Modules.ProfileService)
local WaitFor = require(ReplicatedStorage.Shared.Modules.WaitFor)

local DataService : DataService = Knit.CreateService{
	Name = "DataService",
	ProfileStoreName = string.format(`%s_profile_store_predev`, game.Name), -- basically datastore name
	ProfileStructure = { -- This is just a template, used by profile service
		["Cash"] = {
			CurrentValue = 0,
			StarterValue = 0,
			Type = "NumberValue",
			Parent = "leaderstats", -- either leaderstats or player
		},
		["TimePlayed"] = {
			CurrentValue = 0,
			StarterValue = 0,
			Type = "NumberValue",
			Parent = "Player", 
		},
		["Inventory"] = {
			CurrentValue = {},
			StarterValue = {}, -- u probably noticed but this is a table data, that means that it won't be replicated into an instance but will still be updated
		}
	},
	Profiles = {}, -- where the profiles of each player is stored like this : Profiles[Player].Data[Value]
	Client = {}
}

export type ProfileStoreName = string
export type ProfileStructure = {
	ProfileStoreName : string,
	ProfileStructure : {
		[string] : {
			CurrentValue : {number | string | table | any?},
			StarterValue : number,
			Type : string,
			Parent : string,
		}
	},
}

export type DataService = {
	ProfileStoreName : string,

	fetch : (dataSource : { Player | {Player}}, dataName : string) -> {string | number | table}?,
	set : (dataSource : { Player | {Player}}, dataName : string, newData : {string | number | table}) -> boolean,
	wipe : (dataSource : { Player | {Player}}, dataName : string) -> boolean,
	wipeall : (dataSource : { Player | {Player}}) -> boolean,

	Profiles : {
		[string] : {
			CurrentValue : {number | string | table | any?},
			StarterValue : number,
			Type : string,
			Parent : string,
		}
	},
}

-- this just turns any table into a string
local function stringify(v : table, spaces : number, usesemicolon : boolean, depth : number)
	if not v then return end

	local insert = table.insert

	if type(v) ~= 'table' then
		return tostring(v)
	elseif not next(v) then
		return '{}'
	end

	spaces = spaces or 4
	depth = depth or 1

	local space = (" "):rep(depth * spaces)
	local sep = usesemicolon and ";" or ","
	local concatenationBuilder = {"{"}
		
	for k, x in next, v do
		insert(concatenationBuilder, ("\n%s[%s] = %s%s"):format(space,type(k)=='number'and tostring(k)or('"%s"'):format(tostring(k)), stringify(x, spaces, usesemicolon, depth+1), sep))
	end

	local s = table.concat(concatenationBuilder)
	return ("%s\n%s}"):format(s:sub(1,-2), space:sub(1, -spaces-1))
end

function DataService:KnitInit()
	local ProfileStore : ProfileStructure = ProfileService.GetProfileStore(self.ProfileStoreName :: ProfileStoreName, self.ProfileStructure :: ProfileStructure)

	-- In this function I just load the profile, create values under either leaderstats folder or player and then I wrap it all to a coroutine and update the values every task.wait()
	-- I also connect it to a WaitFor connection that times out within 120 seconds (it just checks if profile is nil, if it is, it closes the coroutines)

	local function loadProfile(player : Player)
		local profile : table = self.Profiles[player :: Player]

		local leaderstats : Folder = Instance.new("Folder", player :: Player)
		leaderstats.Name = "leaderstats"
		
		task.spawn(function()
			local function createValues()
				local operations : table = {}

				-- I just make a list of promises and put them in a table, i check if the data is required to be replicated as an instance otherwise i'll ignore it (likely a table)

				for dataName, dataValues in pairs(self.ProfileStructure) do
					if typeof(dataValues.StarterValue) ~= "table" or typeof(dataValues.CurrentValue) ~= "table" then
						table.insert(operations, Promise.new(function(resolve, reject)
							local dataInstance : Instance = Instance.new(tostring(dataValues.Type), (((dataValues.Parent == "leaderstats" and true) and leaderstats) or player) or reject())
							resolve({dataInstance = dataInstance, dataName = dataName, dataValues = dataValues})
						end))
					end
				end

				-- just simple promises, i just the values to currentValue (only if they are values)
				Promise.all(operations):andThen(function(results)
					for _, result in ipairs(results) do
						local dataInstance, dataName, dataValues = result.dataInstance, result.dataName, result.dataValues
						dataInstance.Name = dataName
						if dataInstance:IsA("IntValue") or dataInstance:IsA("NumberValue") or dataInstance:IsA("StringValue") or dataInstance:IsA("BoolValue") then
							dataInstance.Value = dataValues.CurrentValue
						end
					end
				end):catch(function(err)
					error(err or "An unknown error occured")
				end)
			end

			local function updateValues()
				task.spawn(function()
					-- this is not very complex, i just make a loop and update the data everytime, I don't have to update tables because they are not replicated into an instance

					while task.wait() do
						if not profile then break end

						for dataName, dataValues in pairs(profile.Data) do
							if dataValues.Type == "Data" then return end

							local existingInstance : Instance = (((dataValues.Parent == "leaderstats" and true) and leaderstats:FindFirstChild(dataName)) or player:FindFirstChild(dataName))
							existingInstance.Value = dataValues.CurrentValue
						end
					end
				end)
			end

			local createValuesTask = coroutine.resume(coroutine.create(createValues))
			local updateValuesTask = coroutine.resume(coroutine.create(updateValues))

			task.spawn(function() -- already explained
				WaitFor.Custom(function()
					return profile == nil or nil
				end, 120):andThen(function()
					-- this will only close if profile is nil or not found
					coroutine.close(createValuesTask)
					coroutine.close(updateValuesTask)
				end)
			end) -- check for the first 120 seconds, after that it is not necessary to check anymore
		end)
	end

	-- this is the most important part of the code, i just load the profile and then listen to the release event, if the profile is released, i kick the player
	-- if everything is successful, i just wrap the loadProfile function to a coroutine and then call it with the player as an argument
	-- i also check if the player is still in the game, if not, i release the profile
	-- i also listen to the player removing event, if the player leaves, i release the profile

	local function playerAdded(player : Player)
		--[=[
			local profile = ProfileStore:LoadProfileAsync("Player_".. player.UserId, "ForceLoad")

			@note The LoadProfileAsync takes around 4-5 seconds to return because of back end bad performance
			optimization (ProfileService uses WaitForPendingProfileStore(self) that yields for 4-5 seconds bef
			ore returning the profile.)

			@note1 I've managed to optimize front end for max performance and optimization and reduce the time to around 4.2-4.3 seconds.
		]=]

		local profile = ProfileStore:LoadProfileAsync("Player_".. player.UserId, "ForceLoad")
		if not profile then player:Kick("Unable to load save data. Please Rejoin!") end

		profile:ListenToRelease(function()
			self.Profiles[player] = nil
			player:Kick("Your profile has been loaded remotely. Please Rejoin!")
		end)

		if player:IsDescendantOf(Players) then
			self.Profiles[player] = profile
			coroutine.wrap(loadProfile)(player :: Player)
		else
			profile:Release()
		end
	end

	task.spawn(function()
		for _, Player : Player in ipairs(Players:GetPlayers()) do
			task.spawn(playerAdded, Player :: Player)
		end
	end)

	Players.PlayerAdded:Connect(playerAdded)
	Players.PlayerRemoving:Connect(function(Player : Player)
		local profile = self.Profiles[Player]
		if not profile then return end
		
		profile:Release()
	end)
end

--[=[
	@function self:fetch(dataSource : { Player | {Player}}, dataName : string) : {string | number | table}?
	@brief It can take bulk players or single players as argument, it is required to provide a dataName, and it just basically indexes thru
	self.Profiles and looks for player data
]=]


function DataService:fetch(dataSource : { Player | {Player}}, dataName : string) : {string | number | table}?
	if not next(self.Profiles) then
		error("Profiles not loaded yet, please use `WaitFor.Custom()` to wait for the profiles to load.")
	end

	if typeof(dataSource) == "table" then 
		local foundData = {}

		Promise.new(function(resolve, reject)
			local approvedSources = {}
			for _, player in pairs(dataSource) do
				if (((self.Profiles[player] and true) and self.Profiles[player].Data[dataName].CurrentValue) or nil) then
					table.insert(approvedSources, player)
				end
			end

			resolve(approvedSources)
		end):andThen(function(approvedSources : table)
			for _, player : Player in pairs(approvedSources) do
				foundData[player] = self.Profiles[player].Data[dataName].CurrentValue
			end
		end):catch(function()
			error("Unknown error occured while trying to fetch data")
		end)

		return foundData or nil
	else
		-- when i use things like this its just cause i love one liners, this just checks if the profile exists and if the data exists, if it does, it returns the data, if not, it returns nil
		return (((self.Profiles[dataSource] and true) and self.Profiles[dataSource].Data[dataName].CurrentValue) or nil)
	end
end

--[=[
	@function self:set(dataSource : { Player | {Player}}, dataName : string, newData : {string | number | table}) : boolean
	@returns boolean -> Success Indicator
	@brief It can take bulk players or single players as argument, it is required to provide a dataName & newData
	@brief2 It basically indexes thru self.Profiles[Player].Data[dataName] and sets .CurrentValue to newData 
]=]

function DataService:set(dataSource : { Player | {Player}}, dataName : string, newData : {string | number | table}) : boolean
	if not self:fetch(dataSource, dataName) then return false end
	if not next(self.Profiles) then
		error("Profiles not loaded yet, please use `WaitFor.Custom()` to wait for the profiles to load.")
	end

	if typeof(dataSource) == "table" then 
		local operationSuccess : boolean = true

		Promise.new(function(resolve, reject)
			local approvedSources = {}
			for _, player in pairs(dataSource) do
				if (((self.Profiles[player] and true) and self.Profiles[player].Data[dataName].CurrentValue) or nil) then
					table.insert(approvedSources, player)
				end
			end

			resolve(approvedSources)
		end):andThen(function(approvedSources : table)
			for _, player : Player in pairs(approvedSources) do
				self.Profiles[player].Data[dataName].CurrentValue = newData
			end
		end):catch(function()
			operationSuccess = not operationSuccess
		end)

		return operationSuccess or nil
	else
		self.Profiles[dataSource].Data[dataName].CurrentValue = newData
		return true
	end
end

--[=[
