--[=[
	@function self:wipe(dataSource : { Player | {Player}}) : boolean
	@returns boolean -> Success Indicator
	@brief It can take bulk players or single players as argument
	@brief2 It basically indexes thru self.Profiles[Player].Data and loops thru it to reset every value using .CurrentValue to .StarterValue 
]=]

function DataService:wipeall(dataSource : { Player | {Player}}) : boolean
	if not next(self.Profiles) then
		error("Profiles not loaded yet, please use `WaitFor.Custom()` to wait for the profiles to load.")
	end

	if typeof(dataSource) == "table" then
		local operationSuccess : boolean = true

		Promise.new(function(resolve, reject)
			local approvedSources = {}
			for _, player in pairs(dataSource) do
				if (((self.Profiles[player] and true) and true) or nil) then
					table.insert(approvedSources, player)
				end
			end

			resolve(approvedSources)
		end):andThen(function(approvedSources : table)
			for _, player : Player in pairs(approvedSources) do
				for dataName, _ in pairs(self.Profiles[player].Data) do
					self.Profiles[player].Data[dataName].CurrentValue = self.Profiles[player].Data[dataName].StarterValue
				end
			end
		end):catch(function()
			operationSuccess = not operationSuccess
		end)

		return operationSuccess or nil
	else
		for dataName, _ in pairs(self.Profiles[dataSource].Data) do
			self.Profiles[dataSource].Data[dataName].CurrentValue = self.Profiles[dataSource].Data[dataName].StarterValue
		end
	end
end

return DataService
